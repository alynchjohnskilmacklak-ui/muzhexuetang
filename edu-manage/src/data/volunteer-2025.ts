export interface AllocationLineInfo {
  value: number
  source: 'db'
  label: '分配生录取线'
}

export interface AllocationBand {
  highSchoolName: string
  quota: number
  bandLo: number
  bandHi: number
  tongZhao: number
  allocationLine: AllocationLineInfo | null
  effectiveLine: number
  lineSource: 'db' | 'control'
  tag: '推荐' | '保底' | '排名不足' | '分数不足'
  note: string
}

function getAllocationLineInfo(school: { yiTong: number | null; allocationLine?: number | null }): AllocationLineInfo | null {
  const value = getAllocationLine(school)
  if (value === null) return null
  return { value, source: 'db', label: '分配生录取线' }
}

export function getAllocationBands(
  middleSchool: string,
  rank: number,
  score: number,
  controlLine: number,
  lineLookup: (name: string) => { yiTong: number | null; tongZhao: number; allocationLine: number | null } | null
): AllocationBand[] {
  const quotaMap = XINLE_ALLOCATION_2025[middleSchool] || {}

  const schools = Object.entries(quotaMap)
    .filter(([, q]) => q > 0)
    .map(([name, quota]) => {
      const lines = lineLookup(name)
      const yiTong = lines?.yiTong ?? null
      const tongZhao = lines?.tongZhao ?? 0
      const allocationLine = getAllocationLineInfo({ yiTong, allocationLine: lines?.allocationLine ?? null })
      return { name, quota, yiTong, tongZhao, allocationLine }
    })
    .sort((a, b) => (b.yiTong ?? 0) - (a.yiTong ?? 0))

  let cum = 0
  return schools.map(s => {
    const bandLo = cum + 1
    cum += s.quota
    const bandHi = cum
    const effectiveLine = s.allocationLine?.value ?? controlLine
    const lineSource: 'db' | 'control' = s.allocationLine ? 'db' : 'control'

    let tag: AllocationBand['tag']
    let note = ''
    if (score < effectiveLine) {
      tag = '分数不足'
      note = lineSource === 'db'
        ? `你的分数未达该校分配生录取线（${effectiveLine}分）`
        : `你的分数未达控制线（${effectiveLine}分）`
    } else if (rank > bandHi) {
      tag = '排名不足'
      note = `该校名额对应校内前${bandHi}名，你目前第${rank}名，存在校内排名竞争风险`
    } else if (rank < bandLo) {
      tag = '保底'
      note = `你的排名优于该档（前${bandLo - 1}名通常会竞争更好的学校），此校可重点考虑`
    } else {
      tag = '推荐'
      note = `你的校内第${rank}名落在该校名额区间（第${bandLo}-${bandHi}名），分数也达标，当前模拟结果较匹配`
    }

    return {
      highSchoolName: s.name,
      quota: s.quota,
      bandLo,
      bandHi,
      tongZhao: s.tongZhao,
      allocationLine: s.allocationLine,
      effectiveLine,
      lineSource,
      tag,
      note,
    }
  })
}

// 从 getAllocationBands 结果中提取首选推荐
export function getTopRecommendation(bands: AllocationBand[]): { band: AllocationBand; source: 'recommended' | 'fallback_safe' } | null {
  const recommended = bands.filter(b => b.tag === '推荐')
  if (recommended.length > 0) {
    recommended.sort((a, b) => b.tongZhao - a.tongZhao)
    return { band: recommended[0], source: 'recommended' }
  }
  // 无推荐时，取保底中档位最高的作为稳妥选项
  const safe = bands.filter(b => b.tag === '保底')
  if (safe.length > 0) {
    safe.sort((a, b) => b.tongZhao - a.tongZhao)
    return { band: safe[0], source: 'fallback_safe' }
  }
  return null
}

